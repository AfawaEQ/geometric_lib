def area(a, h): 
    '''Принимает сторону треугольника a и его высоту b, возвращает площадь треугольника со стороной a и высотой b'''
    return a * h / 2 

def perimeter(a, b, c): 
    '''Принимает стороны треугольника a, b и c, возвращает периметр треугольника со сторонами a, b и c'''
    return a + b + c 
